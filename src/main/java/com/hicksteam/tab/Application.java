    private void createUsersAndRoles()
    {
        BCryptPasswordEncoder encoder = new BCryptPasswordEncoder();

        Arrays.asList("eric", "steve").forEach(userName -> {
            // Create a new record
            TabUserRecord user = create.selectFrom(TAB_USER).where(TAB_USER.USERNAME.eq(userName)).fetchAny();
            if (user == null)
            {
                user = create.newRecord(TAB_USER);
                user.setUsername(userName);
                user.setPassword(encoder.encode(userName));
                user.setEnabled(true);
                user.store();
            }

            UserRoleRecord userRole = create.selectFrom(USER_ROLE).where(USER_ROLE.USERNAME.eq(userName)).and(USER_ROLE.ROLE.eq("user")).fetchAny();
            if (userRole == null)
            {
                userRole = create.newRecord(USER_ROLE);
                userRole.setUsername(userName);
                userRole.setRole("user");
                userRole.store();
            }
        });

        List<List<String>> records = new ArrayList<>(Arrays.asList(
                Arrays.asList("Uncle Walt's Band", "Getaway", "content"),
                Arrays.asList("Pink Floyd", "Money", getHotelCaliforniaContent())
        ));
        List<String> types = Arrays.asList("chords", "guitar", "bass");

        Random random = new Random();
        records.forEach(record -> {
            int index = records.indexOf(record);
            int id = index + 1;

            String artist = records.get(index).get(0);
            String title = records.get(index).get(1);
            String content = records.get(index).get(2);

            TabRecord aTab = create.selectFrom(TAB).where(TAB.ID.eq(id)).fetchAny();
            if (aTab == null)
            {
                aTab = create.newRecord(Tables.TAB);
                aTab.setArtist(artist);
                aTab.setTitle(title);
                aTab.setContent(content);
                aTab.setAuthorId((long) random.nextInt(1000));
                aTab.setCreatedOn(new Timestamp(System.currentTimeMillis()));
                aTab.setRating(random.nextDouble() * 5);
                aTab.setVotes(random.nextInt(10000));
                aTab.setVersion(random.nextInt(10));
                aTab.setType(types.get(random.nextInt(types.size())));
                aTab.setViews(0);
                aTab.setSource("dummy data");
                aTab.setContentHash(content.hashCode());
                aTab.store();
            }
        });
    }

